# lists ,sets, tuples, dictionaries in python

fruits = ['apple', 'banana', 'cherry']

print(fruits[0])
print(fruits[1]) 
print(fruits[2])
print(fruits[-1]) 

print(fruits[::-1]) # reverse order

fruits.append('orange')  # add to the end
print(fruits)

fruits.insert(1, 'kiwi')  # add at index 1
print(fruits)